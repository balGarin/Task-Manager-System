CREATE TABLE IF NOT EXISTS users(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
username  VARCHAR(100) UNIQUE NOT NULL,
password VARCHAR(15) NOT NULL,
email VARCHAR(100) UNIQUE NOT NULL,
enabled boolean
);

CREATE TABLE IF NOT EXISTS authorities(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
username VARCHAR(100),
authority VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS tasks(
task_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
title VARCHAR(50) NOT NULL,
description VARCHAR(200) ,
author_id VARCHAR(50) REFERENCES users(id),
executor_id VARCHAR(50) REFERENCES users(id),
status NOT NULL,
priority NOT NULL
);

CREATE TABLE IF NOT EXISTS comments (
 comment_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
 task_id INTEGER REFERENCES tasks(task_id),
  commentText TEXT NOT NULL,
);